package org.juanse.models;

/**
 * Representa el producto final en el patron Builder
 */
public class Computadora {
    /**
     * Indica que contiene el componente procesador
     */
    public String procesador;
    /**
     * Indica que contiene el componente ram
     */
    public int ram;
    /**
     * Indica que contiene el componente almacenamiento
     */
    public int almacenamiento;
    /**
     * Indica que contiene el componente grafica
     */
    public boolean tieneGrafica;
    /**
     * Indica que contiene el comonente LucesRGB
     */
    public boolean tieneLucesRGB;
}